#include "main.h"
/**
 * print_to_98 - function that prints natural numbers to 98
 *
 * @n: function parameter
 *
 * Return: Always 0.
 */
void print_to_98(int n)
{
	int i;

	for (n = i; n <= 98; n++)
	{
		if (n >= 0)
		{
			_putchar(',');
			_putchar(' ');
			_putchar('n');
		}
	}
	_putchar('\n');
}
